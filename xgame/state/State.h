#pragma once

struct Message;

template<class entity_type>
class State
{
public:
	virtual ~State() {}

	//------------------------------------------------------------------------
	// 进入
	//------------------------------------------------------------------------
	virtual void Enter(entity_type*) = 0;

	//------------------------------------------------------------------------
	// 执行
	//------------------------------------------------------------------------
	virtual void Execute(entity_type*) = 0;

	//------------------------------------------------------------------------
	// 退出
	//------------------------------------------------------------------------
	virtual void Exit(entity_type*) = 0;

	//------------------------------------------------------------------------
	// 响应消息
	// @return 返回消息是否被成功的处理了
	//------------------------------------------------------------------------
	virtual bool OnMessage(entity_type*, const Message*) = 0;
};