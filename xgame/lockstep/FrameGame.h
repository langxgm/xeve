#pragma once

#include <string>

class Frame;
class FrameSequence;
class FrameExecutor;
class FrameRoom;

class FrameGame
{
public:
	FrameGame();
	virtual ~FrameGame();

	//------------------------------------------------------------------------
	// 创建帧
	//------------------------------------------------------------------------
	virtual Frame* CreateFrame() = 0;

	//------------------------------------------------------------------------
	// 创建帧序列
	//------------------------------------------------------------------------
	virtual FrameSequence* CreateFrameSequence() = 0;

	//------------------------------------------------------------------------
	// 创建帧执行器
	//------------------------------------------------------------------------
	virtual FrameExecutor* CreateFrameExecutor() = 0;

	//------------------------------------------------------------------------
	// 帧间隔
	//------------------------------------------------------------------------
	virtual int32_t GetFrameInterval() { return 100; }

	//------------------------------------------------------------------------
	// 帧速度
	//------------------------------------------------------------------------
	virtual int32_t GetFrameSpeed() { return 5; }

	//------------------------------------------------------------------------
	// 创建帧同步房间
	//------------------------------------------------------------------------
	virtual FrameRoom* CreateFrameRoom();

public:
	//------------------------------------------------------------------------
	// 初始化
	//------------------------------------------------------------------------
	virtual void Init(const std::string& strName);

public:
	// 名字
	std::string m_strName;
};
