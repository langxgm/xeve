#pragma once

#include <string>

class Module
{
public:
	virtual ~Module() {}

	//------------------------------------------------------------------------
	// 模块名称
	//------------------------------------------------------------------------
	virtual std::string GetName() = 0;

	//------------------------------------------------------------------------
	// 初始化
	//------------------------------------------------------------------------
	virtual bool Init() = 0;

	//------------------------------------------------------------------------
	// 退出
	//------------------------------------------------------------------------
	virtual void Exit() = 0;

	//------------------------------------------------------------------------
	// 运行一次
	//------------------------------------------------------------------------
	virtual void RunOnce() = 0;
};
