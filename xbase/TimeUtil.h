#pragma once

#include <stdint.h>

class TimeUtil
{
public:
	//------------------------------------------------------------------------
	// 获取当前时间(单位:秒)
	// @return time(0)
	//------------------------------------------------------------------------
	static int64_t GetCurrentTimeSeconds();

	//------------------------------------------------------------------------
	// 获取当前时间(单位:毫秒)
	//------------------------------------------------------------------------
	static int64_t GetCurrentTimeMillis();

	//------------------------------------------------------------------------
	// 获取当前时间(单位:微妙)
	//------------------------------------------------------------------------
	static int64_t GetCurrentTimeMicros();

	//------------------------------------------------------------------------
	// 获取当前时间(单位:纳秒)
	//------------------------------------------------------------------------
	static int64_t GetCurrentTimeNanos();

	//------------------------------------------------------------------------
	// 获取当前时间(单位:分)
	//------------------------------------------------------------------------
	static int64_t GetCurrentTimeMinute();

	//------------------------------------------------------------------------
	// 获取当前时间(单位:时)
	//------------------------------------------------------------------------
	static int64_t GetCurrentTimeHour();

	//------------------------------------------------------------------------
	// 获取当前时间(单位:天)
	//------------------------------------------------------------------------
	static int64_t GetCurrentTimeDay();

	//------------------------------------------------------------------------
	// 获取当前时间(单位:周)
	//------------------------------------------------------------------------
	static int64_t GetCurrentTimeWeek();

	//------------------------------------------------------------------------
	// 获得时区(单位:秒)
	//------------------------------------------------------------------------
	static int GetTimeZone();

	//------------------------------------------------------------------------
	// 是不是同一天
	//------------------------------------------------------------------------
	static bool IsSameDayOfSeconds(int64_t t1, int64_t t2);
	static bool IsSameDayOfMillis(int64_t t1, int64_t t2);

	//------------------------------------------------------------------------
	// 是不是同一周
	//------------------------------------------------------------------------
	static bool IsSameWeekOfSeconds(int64_t t1, int64_t t2);
	static bool IsSameWeekOfMillis(int64_t t1, int64_t t2);

	//------------------------------------------------------------------------
	// 转换成字符串
	// @param out[20] "2017-03-03 12:42:00"
	// @return length
	//------------------------------------------------------------------------
	static int ToString(char* out, int64_t ts);

	//------------------------------------------------------------------------
	// 转换成字符串
	// @param out[24] "2017-03-03 12:42:00:000"
	// @return length
	//------------------------------------------------------------------------
	static int ToString2(char* out, int64_t tms);

	//------------------------------------------------------------------------
	// 转换成字符串
	// @param out[11] "2017-03-03"
	// @return length
	//------------------------------------------------------------------------
	static int ToStringYMD(char* out, int64_t ts);

	//------------------------------------------------------------------------
	// 转换成字符串
	// @param out[9] "12:42:00"
	// @return length
	//------------------------------------------------------------------------
	static int ToStringHMS(char* out, int64_t ts);

	//------------------------------------------------------------------------
	// 转换成字符串
	// @param out[13] "12:42:00:000"
	// @return length
	//------------------------------------------------------------------------
	static int ToStringHMSMS(char* out, int64_t tms);

	//------------------------------------------------------------------------
	// 打印时间相关参数
	//------------------------------------------------------------------------
	static void Print();
};
